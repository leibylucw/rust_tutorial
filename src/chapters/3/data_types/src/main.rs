fn main() {
    let x: i32 = -2;
    println!("The value of x is: {}", x);

    let y: f64 = 333.1726345;
    println!("The value of y is: {}", y);

    let z: bool = true;
    println!("The value of z is: {}", z);

    let a: [i32; 5] = [1, 2, 3, 4, 5];
    println!("The value of a[0] is: {}", a[0]);
}
